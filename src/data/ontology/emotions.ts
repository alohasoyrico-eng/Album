export const EMOTION_MAP = new Map(EMOTIONS.map((e) => [e.id, e]));

export function getEmotionsByTribe(tribeId: string): Emotion[] {
  return EMOTIONS.filter((e) => e.tribe === tribeId);
}

export function getRelatedEmotions(emotionId: string): Emotion[] {
  const emotion = EMOTION_MAP.get(emotionId);
  if (!emotion) return [];
  const ids = new Set([...emotion.neighbors, ...emotion.antonyms]);
  return EMOTIONS.filter((e) => ids.has(e.id));
}
